public class Contact {
    String kname,kemail,kuname,kpass;

    public void setName(String kname){
        this.kname=kname;
    }

    public String getName() {
        return this.kname;
    }

    public void setEmail(String kemail){
        this.kemail=kemail;
    }

    public String getEmail() {
        return this.kname;
    }

    public void setUname(String kuname){
        this.kuname=kuname;
    }

    public String getUname() {
        return this.kuname;
    }

    public void setPass(String kpass){
        this.kpass=kpass;
    }

    public String getPass() {
        return this.kpass;
    }


}
